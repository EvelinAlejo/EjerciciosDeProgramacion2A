// Clase padre
class Cuenta {
    constructor(numeroDeCuenta, saldo, tipo, nombre) {
        this.numeroDeCuenta = numeroDeCuenta;
        this.saldo = saldo;
        this.tipo = tipo;
        this.nombre = nombre;
    }

    deposito(monto) {
        if(monto > 0) {
            this.saldo += monto;
            console.log(`DEPÓSITO REALIZADO. NUEVO SALDO: ${this.saldo}`);
        }
    }

    retirar(monto) {
        if(monto <= this.saldo) {
            this.saldo -= monto;
            console.log(`c`);
        } else {
            console.log("SALDO INSUFICIENTE");
        }
    }

    consultarSaldo() {
        return this.saldo;
    }
}

// Clase Ahorro
class Ahorro extends Cuenta {
    constructor(numeroDeCuenta, saldo, tipo, nombre, intereses = 0.1) {
        super(numeroDeCuenta, saldo, tipo, nombre);
        this.intereses = intereses;
    }

    aplicarIntereses() {
        let interes = this.saldo * this.intereses;
        this.saldo += interes;
        console.log(`INTERÉS APLICADO: $${interes}. NUEVO SALDO: ${this.saldo}`);
    }

    // Sobrescribir retirar para que NO permita sobregiro
    retirar(monto) {
        if(monto <= this.saldo) {
            this.saldo -= monto;
            console.log(`RETIRO DE AHORRO EXITOSO. NUEVO SALDO: ${this.saldo}`);
        } else {
            console.log("❌ NO PUEDES RETIRAR MÁS DEL SALDO DISPONIBLE");
        }
    }
}

// Clase Corriente
class Corriente extends Cuenta {
    constructor(numeroDeCuenta, saldo, tipo, nombre, limite = 2000) {
        super(numeroDeCuenta, saldo, tipo, nombre);
        this.limite = limite;
    }

    // Sobrescribir retirar para permitir sobregiro
    retirar(monto) {
        if(monto <= this.saldo + this.limite) {
            this.saldo -= monto;
            console.log(`RETIRO DE CORRIENTE EXITOSO (SOBREGIRO PERMITIDO). NUEVO SALDO: ${this.saldo}`);
        } else {
            console.log(" LÍMITE DE SOBREGIRO EXCEDIDO");
        }
    }
}

